<div class="main">The first ten seconds are included to remind you of the usual verse patterns in
the song. At :11, the most prominent electric drops out and the countermelody electric abbreviates
its chord patterns, while the bass takes up a stripped down version of the melody line. At :21,
a synth fades in that sounds, to me, just like "Where the Streets Have No Name" (when you go listen
to that, try to listen underneath the intro organ to hear what I mean). I expect the Edge to jump
in at any moment with some ethereal arpeggios, but it never happens.
