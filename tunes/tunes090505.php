<div class="main">Lots of people love this intro, but it takes a decent pair of headphones to dig
into individual pieces to try to figure out what makes it go. In this excerpt, we get eight seconds
of the beginning of the intro: not a lot going on here with cymbal hits on the beat and a 12 string
acoustic rhythm underpinning Chris Cornell's signature testicular wail. At :8, we get a prominent
clean electric melody over the acoustic, and two things begin to happen that we can't hear unless
we get to hear the stereo channels separately. First, Cornell adds in backing vocals several
octaves down, which are almost exclusively lurking on the left side of the mix. I'd listened to
this song dozens of times before I noticed this; it's really hard to hear without headphones.
Second, on the right side and way low in the mix, an interesting bassline, which is most prominent
at :11-12, and most flavorful at :18.5 (Right before "Out in the sunshine.."), when the bass hits a
couple of notes that feel at once on and off key, and serves to ratchet up the tension even further
into the verse at :31. This is where my lack of serious music theory background lets me down; I'm
not sure what key accommodates those bass notes with the established melody.
