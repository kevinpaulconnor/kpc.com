<div class="main">Kweli often peels away instrumentation as he winds down a song, which provides
us a nice window into his process. At :8 we lose a subtle bongo rhythm and the synth cymbal shuffle
and the sampled vocals, leaving us with two different acoustic guitars (one playing syncopated
chords and the other on a sixteenth note countermelody line), underneath the high synth chords. The
acoustic chords go away at :20, and the synth starts to fade as well, but notice how it jumps back
up in the mix at :21; this is a little clumsy. In the last ten seconds, we can finally hear how the
countermelody acoustic is actually <i>two</i> acoustics (you can hear this best if you listen for
one on the left side of the mix and one on the right side), or one on a slight delay: it&#39;s
difficult to tell.
