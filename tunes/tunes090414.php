<div class="main">The most interesting thing is how the beginning sampled horn track
repeats twice in mono; then the same horn theme is recorded live in stereo and played
twice. Neat! The overall result is that the same notes and instrumentation move up in
the mix just through the recording technique. One reason that I am a
Blackalicious fan is that they often experiment with recording techniques influencing
song composition in this sort of way. Then overall volume fades up and a prominent
bassline establishes a mellow groove. Space alien vocals layer on top-this song was
