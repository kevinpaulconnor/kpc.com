<div class="main">Inspired by the work of minimalist composer Terry Riley, the
introductory synthesizer theme is surely familiar. I want you to focus in on how
the sound of the synthesizer appears to &quot;change&quot; when the piano comes in.
The theme is (I think) an F repeating bass note (duh duh duh duh duh) with an arpeggio
over it. When the F piano chord comes in at about :12.5, the formerly prominent
bassline is subsumed under the piano, and the arpeggio rises to prominence. It
sounds like the synthesizer theme has changed along with the piano introduction, and
continues to change when the piano changes chords. But it hasn&#39;t, and doesn&#39;t.
I love this effect.
