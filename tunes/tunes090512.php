<div class="main">I want my hip-hop backing tracks to be either extremely straightforward and minimal
or thoroughly lush and overproduced; when the extremes get muddied, I start to get bored. This
track is an aggressive instance of the former class; the bass and percussion are no nonsense
pounding, and the additional of sparse, metallic piano and female vocal trills midway through are
the perfect accents to Eminem's vocal line (which I could take or leave here).
</div>
</div>
