<div class="main">Along similar lines as the Eminem track, when you have really great voices, all
you need is bass chords and clapping. The whole song should be more like this; don't need the guitar
and bells and synth and heavy percussion and electric piano cluttering the redacted first five
minutes  of the backing track. I used to suffer through this song when it was big time, waiting for
the end. My recurring problem with contemporary R&B (yes, this still counts!) is boring, distracting
production.
</div>
