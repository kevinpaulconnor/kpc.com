<div class="main">Starting off with Soundgarden for the second straight week, we hear three
interplaying guitars at the end of the intro (one is just holding a high harmonic); they're not
<i>quite</i> in tune with each other, which might be on purpose and might not. Bass picks up the
main verse line at :6, and the left channel guitar fades quickly while the right channel guitars
linger underneath. Listen to the percussion hits; it seems like the drummer is hitting something
with a bit of a tune early on, particularly from :10 to :14, like a cowbell, but it fades to just a
snare by the latter part of the segment. Cornell and bass-mimicing electric come in at :20. The
fuzz guitar "fart" at :25 is a nice touch.
</div>
