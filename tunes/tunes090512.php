Now <i>this</i> is how you introduce your album. Let the horns ride in a little lazy, ease into some
chords with an absolutely blasted baritone sax note sending us into syncopated clean guitar. Bring
in another guitar with a sproinging square wave effect for counter melody for a few notes, then
crash in the rest of the band. This thirty seconds makes me think that the album will be like a
jaunty stroll through a busy city park on a spring day, which is more or less what ensues.
