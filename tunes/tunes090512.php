<div class="main">This song is one of the main inspirations for the Tuesday Tunes feature series; it
has an effect I talked about a few weeks ago with 'Bleeding Me': an off-key guitar lead pattern that
holds steady while the chords move underneath to bring it in tune-the pattern starts at :18.5 and
the chords move at :22. I thought this part was super cool and then thought that I should go through
some other parts of songs that were cool and write about them. And here we are!
