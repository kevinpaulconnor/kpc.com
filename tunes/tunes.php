<div class="mainhead">About Tuesday Tunes</div>
<div class="main">Tuesday Tunes is a short weekly music feature. Every Tuesday,
I'll post five short segments highlighting particular features of songs that I like,
with a paragraph of commentary explaing what is noteworthy about each segment.
I've divided each segment into three categories. <b>"Build"</b> segments are usually
intros or outros, though not always. Their salient feature is that they add or remove
particular instruments or sounds, a few at a time, over several or many bars, creating
varied layers over a particular theme. <b>"Variations"</b> happen when, after certain
pattern has already been established in the song, the pattern is repeated, with a slight
change in instrumentation or bass line or countermelody that makes the repeated pattern
stand out in a fresh way. Variations are often short, and usually, the original
pattern returns for the bulk of the song. <b>"Interludes"</b> are song segments that
don't initially seem to have much to do with the established feel of the song,
but upon further consideration both fit perfectly with the other parts and are the standout
parts of the song, that make it good. </div>
<div class="main">I have a complete list of the songs that I want to go through; I'm going
to do it in alphabetical order, five at a time. I have a little music theory training,
but not a lot; almost everything that I say will come from a layman's perspective.
All of these songs could be categorized as pop music in the broad sense of that term;
you'll find rap and folk-rock but no orchestral music or jazz. Finally, I am not and have
never been someone whose taste lies on the cutting edge of popular music and I mostly
dropped out of listening to new stuff once I moved into my mid-20s. All of which is to
say that if you are a popular music aficionado, you are unlikely to hear more than
five or ten snippets over the course of the project that you haven't heard before; the goal
isn't to introduce listeners to music, but I hope that the recontextualization of
these song segments with my commentary and alongside one another makes this valuable
for you. Happy listening!
</div>
