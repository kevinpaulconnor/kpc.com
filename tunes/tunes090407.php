<div class="main">The Black Eyed Peas went from relevant underground hip hoppers
to wholehearted beer commercial sellouts in record time. This forced fans of their
earlier work to be that guy who liked them &quot;before they made it big&quot; or alternately
look like fans of their bubblegum flavor of the moment corporate phase, epitomized
by their &quot;Let&#39;s Get It Started&quot; hit which began life as an ode to substance abuse,
and pushing musically irrelevant but attractive Fergie into public consciousness.
Kudos to them for cashing in at the right time, but one of the few strong musical
results is this collaboration with Papa Roach. The thing to pay attention to is
the guitar lick; for the first bars of the verse, it mimics will.i.am&#39;s lyric; in this segment,
it has dropped out altogether and then switches to a catchy, superquick sixteenth note crunch, which
is the perfect line transitionally between the tense, tight early verse and the
chorus, which unmercifully crushes and destroys all comers. 
