<div class="main">A wall of sound interlude with subtle variations throughout; a prototype for lots
of layered, crunchy music to come. Prominent guitar riff on the left side of the mix starting at :6
sets the tone for tuneless Moog scritch-scratch and a bouncy bassline. All instruments switch to
pounding out eighth notes at :20 while a new wailing guitar reminds us how the chorus goes, up high.
Rudimentary drum break to cleanse the palate, and we're back in to the main line.
