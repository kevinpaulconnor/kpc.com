<div class="main">An otherwise pedestrian solo contains a schema that I find pleasing: when the
lead guitar anticipates chord movement by settling into a repeating line that is out of tune with
the chords until they change, making the dischordant riff suddenly, jarringly harmonious. The guitar
solo starts out in key over the chords, but at :6, guitarist Kirk Hammett moves into a sixteenth
note pull-off melody that he repeats through :11. Pay close attention just before :8, when the
chords underneath change and Hammett's riff moves into tune without moving at all, creating a moment
of tension. I really like this effect and will return to it in future Tuesday Tunes.
</div>
