<div class="main">This classic runs through four distinct segments in thirty seconds-the kitchen
sink approach to a variation. Start out with Slash's guitar lick trading prominence with
bass/acoustic in the main theme. At :8, we get eight bars of one-off Michael telling us what
he ain&#39;t, with slick synth electric piano (maybe?) accompaniment. The last four bars add in
that jammed-down wah pedal electric sound I've explored before. At :25, everything drops out for
a piano/space zipper arpeggio synth sound, and then we steam on into what sounds like maybe an out
of tune synth acoustic guitar syncopation with heavy synth drums and a sort of lousy rap line on
top. Delicious!
