The boring big wall of noise throughout this song doens't play as well here as it does for
Steppenwolf, and I always thought that this quiet part was the best part of the song. The mildly
distorted guitar apes the usual chord pattern, and a quiet piano drops into the background before
the lazy smashed loudness comes back. I'd be interested in hearing something flipped around, like a
solo piano arrangement with the canonical sound appearing for a bit in a middle eight.
